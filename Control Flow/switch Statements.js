//switch Statements
let moonPhase = 'mostly full';
switch (moonPhase) {
  case 'full':
	 console.log('Howl!');
   break;
  case 'mostly full':
	 console.log('Arms and legs are getting harier');
	 break;
  case 'mostly new':
	 console.log('Back on two feet.');
	 break;
  default:
	 console.log('Invalid moon phase.');
   break;
}